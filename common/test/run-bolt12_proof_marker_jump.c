/* Reproduce BOLT 12 spec contradiction PR #1295 in CLN's reader.
 *
 * Spec discussion:
 *   https://github.com/lightning/bolts/pull/1295#discussion_r3286972971
 *
 * In `lightning/bolts#1295` (head db3eff3a54, file 12-offer-encoding.md):
 *
 *   - Writer rule (lines 1041-1049) mandates that the marker number after a
 *     `proof_omitted_tlvs` entry of 239 is 1000000000 — i.e. the writer skips
 *     past the signature/payer-proof range (TLV types 240..999999999).
 *   - Reader rule (lines 1065-1067) only accepts the next entry if it is
 *     `prev + 1` or `<included TLV> + 1`.  No carve-out for the 239 → 1000000000
 *     jump.
 *
 * A reader applying the spec literally rejects a marker sequence the writer
 * rule mandates.
 *
 * CLN's reader is a literal transcription of the bogus reader rule; see
 * `check_payer_proof` in common/bolt12_proof.c lines 339-388 (specifically the
 * `omitted != prev_omitted + 1` branch at line 379).  Feeding it the
 * writer-rule-mandated sequence `[1, 2, …, 239, 1000000000]` produces an error
 * naming `1000000000` as "not one greater than the previous 239 nor an
 * included tlv entry" — that's the contradiction.
 *
 * This test deliberately FAILS until reader rule 1065 is amended to allow the
 * 239 → 1000000000 jump.  The failure IS the reproduction.
 *
 * Parallel LDK reproduction lives in lightningdevkit/rust-lightning#4297, test
 * `spec_writer_reader_rules_contradict_on_gap_jump` in
 * lightning/src/offers/merkle.rs (commit 0034c79ae).
 */
